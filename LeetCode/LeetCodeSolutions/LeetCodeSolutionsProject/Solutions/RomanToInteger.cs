using System;
using System.Collections.Generic;

namespace LeetCodeSolutions
{
    /// <summary>
    /// حل مسألة LeetCode رقم 13: Roman to Integer
    /// رابط المسألة: https://leetcode.com/problems/roman-to-integer/
    /// </summary>
    /// <remarks>
    /// **الوصف:**
    /// بالنظر إلى رقم روماني، قم بتحويله إلى عدد صحيح.
    /// الأرقام الرومانية عادة ما تكتب من الأكبر إلى الأصغر من اليسار إلى اليم.
    /// ومع ذلك، فإن الرقم أربعة لا يكتب IIII. بدلاً من ذلك، يكتب أربعة IV.
    /// لأن الواحد يأتي قبل الخمسة، فإننا نطرحه، مما يجعل الرقم أربعة.
    /// ينطبق نفس المبدأ على تسعة، والتي تكتب IX.
    /// هناك ست حالات يتم فيها استخدام الطرح:
    /// I يمكن وضعها قبل V (5) و X (10) لتكوين 4 و 9.
    /// X يمكن وضعها قبل L (50) و C (100) لتكوين 40 و 90.
    /// C يمكن وضعها قبل D (500) و M (1000) لتكوين 400 و 900.
    /// 
    /// **مثال 1:**
    /// الإدخال: s = "III"
    /// الإخراج: 3
    /// الشرح: III = 3.
    /// 
    /// **مثال 2:**
    /// الإدخال: s = "LVIII"
    /// الإخراج: 58
    /// الشرح: L = 50, V= 5, III = 3.
    /// 
    /// **مثال 3:**
    /// الإدخال: s = "MCMXCIV"
    /// الإخراج: 1994
    /// الشرح: M = 1000, CM = 900, XC = 90 و IV = 4.
    /// </remarks>
    public class RomanToIntegerSolution
    {
        /// <summary>
        /// يقوم بتحويل الرقم الروماني المعطى إلى عدد صحيح.
        /// </summary>
        /// <param name="s">الرقم الروماني كسلسلة نصية.</param>
        /// <returns>العدد الصحيح المقابل للرقم الروماني.</returns>
        public int RomanToInt(string s)
        {
            // ننشئ قاموساً (Dictionary) لربط كل حرف روماني بقيمته العددية.
            Dictionary<char, int> romanValues = new Dictionary<char, int>
            {
                { 'I', 1 },
                { 'V', 5 },
                { 'X', 10 },
                { 'L', 50 },
                { 'C', 100 },
                { 'D', 500 },
                { 'M', 1000 }
            };

            int total = 0;
            // نبدأ من نهاية السلسلة النصية (الرقم الروماني) ونضيف قيمة آخر حرف.
            // هذا يسهل التعامل مع حالات الطرح (مثل IV و IX).
            total = romanValues[s[s.Length - 1]];

            // نمر على الحروف من اليمين إلى اليسار (باستثناء الحرف الأخير الذي تمت إضافته بالفعل).
            for (int i = s.Length - 2; i >= 0; i--)
            {
                // إذا كانت قيمة الحرف الحالي أقل من قيمة الحرف الذي يليه (في السلسلة الأصلية).
                // فهذا يعني أننا في حالة طرح (مثل I قبل V في IV).
                if (romanValues[s[i]] < romanValues[s[i + 1]])
                {
                    total -= romanValues[s[i]]; // نطرح قيمة الحرف الحالي.
                }
                else
                {
                    total += romanValues[s[i]]; // وإلا، نضيف قيمة الحرف الحالي.
                }
            }

            return total;
        }
    }
}
