using System;

namespace LeetCodeSolutions
{
    /// <summary>
    /// حل مسألة LeetCode رقم 26: Remove Duplicates from Sorted Array
    /// رابط المسألة: https://leetcode.com/problems/remove-duplicates-from-sorted-array/
    /// </summary>
    /// <remarks>
    /// **الوصف:**
    /// بالنظر إلى مصفوفة أعداد صحيحة `nums` مصنفة بترتيب غير تنازلي،
    /// قم بإزالة التكرارات في مكانها بحيث يظهر كل عنصر مرة واحدة فقط.
    /// يجب أن يكون الترتيب النسبي للعناصر هو نفسه.
    /// 
    /// نظراً لأنه من المستحيل تغيير طول المصفوفات في بعض اللغات،
    /// يجب أن تكون النتيجة موضوعة في الجزء الأول من المصفوفة `nums`.
    /// على سبيل المثال، إذا كان `k` هو عدد العناصر الفريدة،
    /// فيجب أن تحتوي `nums` على أول `k` عناصر فريدة بالترتيب الصحيح.
    /// يجب أن تكون الأجزاء المتبقية من `nums` غير مهمة وكذلك حجمها.
    /// 
    /// أعد العدد `k` بعد وضع النتيجة في أول `k` خانات من `nums`.
    /// يجب ألا تخصص مساحة إضافية للمصفوفة. يجب أن تفعل ذلك عن طريق تعديل مصفوفة الإدخال في مكانها بذاكرة إضافية O(1).
    /// 
    /// **مثال 1:**
    /// الإدخال: nums = [1,1,2]
    /// الإخراج: 2, nums = [1,2,_]
    /// الشرح: يجب أن تكون دالتك قد أعادت k = 2، مع أول عنصرين من nums هما 1 و 2 على التوالي.
    /// ما يهمك هو أول k عناصر، وما تبقى من المصفوفة لا يهم.
    /// 
    /// **مثال 2:**
    /// الإدخال: nums = [0,0,1,1,1,2,2,3,3,4]
    /// الإخراج: 5, nums = [0,1,2,3,4,_,_,_,_,_]
    /// الشرح: يجب أن تكون دالتك قد أعادت k = 5، مع أول خمسة عناصر من nums هي 0، 1، 2، 3، و 4 على التوالي.
    /// ما يهمك هو أول k عناصر، وما تبقى من المصفوفة لا يهم.
    /// </remarks>
    public class RemoveDuplicatesFromSortedArraySolution
    {
        /// <summary>
        /// يزيل التكرارات من مصفوفة مصنفة في مكانها.
        /// </summary>
        /// <param name="nums">المصفوفة المصنفة.</param>
        /// <returns>عدد العناصر الفريدة (k).</returns>
        public int RemoveDuplicates(int[] nums)
        {
            // إذا كانت المصفوفة فارغة، فلا توجد تكرارات لإزالتها.
            if (nums == null || nums.Length == 0)
            {
                return 0;
            }

            // مؤشر 'i' يتتبع الموضع التالي للعنصر الفريد.
            // يبدأ من 1 لأن العنصر الأول (nums[0]) يعتبر دائماً فريداً.
            int i = 1;

            // مؤشر 'j' يمر على جميع عناصر المصفوفة من العنصر الثاني.
            for (int j = 1; j < nums.Length; j++)
            {
                // إذا كان العنصر الحالي (nums[j]) مختلفاً عن العنصر السابق (nums[j-1])،
                // فهذا يعني أنه عنصر فريد.
                if (nums[j] != nums[j - 1])
                {
                    // نضع العنصر الفريد في الموضع 'i' ونزيد 'i' للموضع التالي.
                    nums[i] = nums[j];
                    i++;
                }
            }

            // 'i' يمثل الآن عدد العناصر الفريدة (k).
            return i;
        }
    }
}
