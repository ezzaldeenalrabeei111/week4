using System;
using System.Collections.Generic;

namespace LeetCodeSolutions
{
    /// <summary>
    /// حل مسألة LeetCode رقم 20: Valid Parentheses
    /// رابط المسألة: https://leetcode.com/problems/valid-parentheses/
    /// </summary>
    /// <remarks>
    /// **الوصف:**
    /// بالنظر إلى سلسلة نصية `s` تحتوي فقط على الأحرف '(', ')', '{', '}', '[' و ']'،
    /// حدد ما إذا كانت سلسلة الإدخال صالحة.
    /// 
    /// السلسلة النصية للإدخال صالحة إذا:
    /// 1. يجب أن تفتح الأقواس من نفس النوع من الأقواس.
    /// 2. يجب أن تفتح الأقواس بالترتيب الصحيح.
    /// 3. يجب أن يكون لكل قوس فتح قوس إغلاق مطابق من نفس النوع.
    /// 
    /// **مثال 1:**
    /// الإدخال: s = "()"
    /// الإخراج: true
    /// 
    /// **مثال 2:**
    /// الإدخال: s = "()[]{}"
    /// الإخراج: true
    /// 
    /// **مثال 3:**
    /// الإدخال: s = "(]"
    /// الإخراج: false
    /// </remarks>
    public class ValidParenthesesSolution
    {
        /// <summary>
        /// يتحقق مما إذا كانت سلسلة الأقواس المعطاة صالحة.
        /// </summary>
        /// <param name="s">سلسلة الأقواس للتحقق.</param>
        /// <returns>true إذا كانت الأقواس صالحة، وإلا false.</returns>
        public bool IsValid(string s)
        {
            // نستخدم Stack لتتبع الأقواس المفتوحة.
            Stack<char> stack = new Stack<char>();

            // نمر على كل حرف في السلسلة النصية.
            foreach (char c in s)
            {
                // إذا كان الحرف قوس فتح، ندفعه إلى الـ Stack.
                if (c == '(' || c == '{' || c == '[')
                {
                    stack.Push(c);
                }
                // إذا كان الحرف قوس إغلاق.
                else
                {
                    // إذا كان الـ Stack فارغاً، فهذا يعني أن هناك قوس إغلاق بدون قوس فتح مطابق.
                    if (stack.Count == 0)
                    {
                        return false;
                    }

                    // نسحب آخر قوس فتح من الـ Stack.
                    char top = stack.Pop();

                    // نتحقق مما إذا كان قوس الإغلاق الحالي يطابق قوس الفتح الذي سحبناه.
                    if ((c == ')' && top != '(') ||
                        (c == '}' && top != '{') ||
                        (c == ']' && top != '['))
                    {
                        return false;
                    }
                }
            }

            // بعد المرور على جميع الأحرف، إذا كان الـ Stack فارغاً، فهذا يعني أن جميع الأقواس المفتوحة
            // قد تم إغلاقها بشكل صحيح. وإلا، فهناك أقواس فتح لم يتم إغلاقها.
            return stack.Count == 0;
        }
    }
}
